  <h1>Readme</h1>  

    <h2>Consideraciones Generales</h2>

    <h3>Diseño</h3>
    <p>
      Se utilizaron cookies para permitir guardar, y por lo tanto recordar localmente por un tiempo determinado el estilo elegido por el usuario. Para el caso de los valores obtenidos mediante el ingreso del usuario en los campos correspondientes, se utilizó la tecnología LocalStorage.
      Con respecto a la estructura de la página, básicamente se generan automáticamente todos los grupos mediante javascript, como así también la sección de playoffs.
    </p>

    <h3>Implementación</h3>
    <p>La implementación del sitio se encuentra dividida en dos archivos principales:
        <ul>
          <li>fixture.js:
            <p>Carga el XML, lo parsea y se generan los grupos como así también cierta lógica asociada al ordenamiento automático de las tablas y consideraciones de aspecto gráfico</p>
          </li>
          <li>script.js:
            <p>En este archivo javascript se realizan funciones como por ejemplo el manejo de la carga aleatoria de datos, borrado automático de todos los campos, reseteo de tablas y otras consideracioes de diseño gráfico.
            </p>
          </li>
        </ul>

        Además se utilizaron librerías javascript  para que la presentación de la información fuera más vistosa: JQuery, JQueryUI (Dialog), JQuery Qtip (Tooltip) y JqueryTablesorter para el ordenamiento automático de la tablas.<br/>
    </p>

    <h3>Pruebas y Limitaciones</h2>
    <p>
      Se probó el sitio web en Mozilla Firefox, Google Chrome, Opera, Safari y Microsoft Internet Explorer. Se pudo observar, y como era probable de suceder, que en los distintos navegadores y versiones pueden llegar a haber algunos cambios especialmente en la parte gráfica.
      Una limitación de este proyecto es que no podrá apreciarse correctamente a distintas resoluciones, especialmente en 800*600. Para las demás resoluciones el proyecto pudo observarse correctamente.
    </p>

